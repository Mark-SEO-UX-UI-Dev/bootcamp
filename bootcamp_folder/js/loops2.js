function runActivity() {
  // WHILE Loop
  let x = 0;

  while (x <= 5) {
    console.log(x);
    x++;
    break;
    // BREAK is used to prematurely stop the iteration
    // Anything under the break; code will not be executed
    alert();
  }
}
